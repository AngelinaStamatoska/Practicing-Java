public class Calculator {
    public static int addMethod(int first, int second) {

        return first + second;
    }
    public static int subMethod(int first, int second) {

        return first - second;
    }
    public int mulMethod (int first, int second) {
        return first*second;
    }
    public int divMethod (int first, int second) {
        return first/second;
    }
}
